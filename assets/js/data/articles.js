/* ============================================================
   Helper that the magazine page + admin CMS both use
   ============================================================ */
window.ipartmentLoadArticles = function() {
  const defaults = window.IPARTMENT_DEFAULT_ARTICLES || [];
  let userAdded = [];
  let hiddenIds = [];
  let edits = {};
  try {
    userAdded = JSON.parse(localStorage.getItem('ipartment_magazine_articles') || '[]');
    hiddenIds = JSON.parse(localStorage.getItem('ipartment_magazine_hidden') || '[]');
    edits = JSON.parse(localStorage.getItem('ipartment_magazine_edited') || '{}');
  } catch (e) { /* swallow */ }

  // Apply edits to defaults, then drop hidden ones
  const editedDefaults = defaults
    .filter(a => !hiddenIds.includes(a.id))
    .map(a => edits[a.id] ? Object.assign({}, a, edits[a.id]) : a);

  // User-added go first (newest first), then edited defaults
  const sortedUserAdded = userAdded.slice().sort((a, b) => (b.createdAt || '').localeCompare(a.createdAt || ''));
  return sortedUserAdded.concat(editedDefaults);
};

// CMS write helpers - used by admin.js
window.ipartmentMagazine = {
  add(article) {
    const list = JSON.parse(localStorage.getItem('ipartment_magazine_articles') || '[]');
    const id = 'usr-' + Date.now().toString(36).toUpperCase() + '-' + Math.random().toString(36).slice(2,6).toUpperCase();
    const stamped = Object.assign({ id, createdAt: new Date().toISOString() }, article);
    list.push(stamped);
    localStorage.setItem('ipartment_magazine_articles', JSON.stringify(list));
    return stamped;
  },
  update(id, patch) {
    // User-added article? Update the row directly.
    let list = JSON.parse(localStorage.getItem('ipartment_magazine_articles') || '[]');
    const idx = list.findIndex(a => a.id === id);
    if (idx > -1) {
      list[idx] = Object.assign({}, list[idx], patch);
      localStorage.setItem('ipartment_magazine_articles', JSON.stringify(list));
      return list[idx];
    }
    // Default article? Store an edit overlay.
    const edits = JSON.parse(localStorage.getItem('ipartment_magazine_edited') || '{}');
    edits[id] = Object.assign({}, edits[id] || {}, patch);
    localStorage.setItem('ipartment_magazine_edited', JSON.stringify(edits));
    const def = (window.IPARTMENT_DEFAULT_ARTICLES || []).find(a => a.id === id);
    return def ? Object.assign({}, def, edits[id]) : null;
  },
  remove(id) {
    // User-added? Hard delete.
    let list = JSON.parse(localStorage.getItem('ipartment_magazine_articles') || '[]');
    const before = list.length;
    list = list.filter(a => a.id !== id);
    if (list.length !== before) {
      localStorage.setItem('ipartment_magazine_articles', JSON.stringify(list));
      return { mode: 'deleted' };
    }
    // Default? Add to hidden list.
    let hidden = JSON.parse(localStorage.getItem('ipartment_magazine_hidden') || '[]');
    if (!hidden.includes(id)) hidden.push(id);
    localStorage.setItem('ipartment_magazine_hidden', JSON.stringify(hidden));
    return { mode: 'hidden' };
  },
  restoreDefault(id) {
    let hidden = JSON.parse(localStorage.getItem('ipartment_magazine_hidden') || '[]');
    hidden = hidden.filter(h => h !== id);
    localStorage.setItem('ipartment_magazine_hidden', JSON.stringify(hidden));
    // Also drop any edits so it's truly restored
    const edits = JSON.parse(localStorage.getItem('ipartment_magazine_edited') || '{}');
    delete edits[id];
    localStorage.setItem('ipartment_magazine_edited', JSON.stringify(edits));
  },
  getAllForAdmin() {
    // Show defaults (with edit overlay) AND user-added AND hidden state
    const defaults = window.IPARTMENT_DEFAULT_ARTICLES || [];
    const userAdded = JSON.parse(localStorage.getItem('ipartment_magazine_articles') || '[]');
    const hiddenIds = JSON.parse(localStorage.getItem('ipartment_magazine_hidden') || '[]');
    const edits = JSON.parse(localStorage.getItem('ipartment_magazine_edited') || '{}');
    const decoratedDefaults = defaults.map(a => Object.assign({}, a, edits[a.id] || {}, { _kind: 'default', _hidden: hiddenIds.includes(a.id) }));
    const decoratedUserAdded = userAdded.map(a => Object.assign({}, a, { _kind: 'user' }));
    return decoratedUserAdded.concat(decoratedDefaults);
  },
  getById(id) {
    return this.getAllForAdmin().find(a => a.id === id);
  }
};
